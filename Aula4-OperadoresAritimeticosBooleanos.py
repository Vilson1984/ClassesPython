# OPERADORES MATEMÁTICOS
# Soma: + / Subtração: - / Multi: * / Divis: / 
# Divisão inteira: //
# Resto da divisão: %
# Potência: **

numero1 = 20
numero2 = 10

print(numero1-numero2)
print(numero1+numero2)
print(numero1*numero2)
print(numero1/numero2)
print(numero1//numero2)
print(numero1%numero2)
print(numero1**numero2)

# OPERADORES BOOLEANOS
# > 
# <
